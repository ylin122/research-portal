// Weekly cron — pulls GPU pricing from AWS (Vantage), Azure (Retail Prices API),
// and GCP (Cloud Billing Catalog), classifies SKUs against the cohort map, and
// upserts into compute_prices. Triggered Sundays via vercel.json.
//
// Manual trigger:
//   curl -H "Authorization: Bearer $CRON_SECRET" https://<host>/api/cron-compute-pricing
//
// Required env vars (Vercel):
//   SUPABASE_URL                  (or VITE_SUPABASE_URL)
//   SUPABASE_SERVICE_ROLE_KEY     (writes bypass RLS via service role)
//   GCP_API_KEY                   (Cloud Billing API; free tier)
//   CRON_SECRET                   (auto-generated by Vercel for cron auth)

import { createClient } from "@supabase/supabase-js";
import { COHORT_MAP, classifySku } from "../src/data/computeCohortMap.js";
import { NEOCLOUD_PRICES } from "../src/data/neocloudPrices.js";

// In Vercel production, env vars are injected automatically.
// In local `vercel dev`, they aren't reaching serverless functions reliably,
// so we explicitly load .env.local. dotenv silently no-ops if the file is
// missing or vars are already set, so it's safe in production too.
if (!process.env.SUPABASE_SERVICE_ROLE_KEY) {
  try {
    const { fileURLToPath } = await import("node:url");
    const path = await import("node:path");
    const dotenv = await import("dotenv");
    const here = path.dirname(fileURLToPath(import.meta.url));
    dotenv.config({ path: path.join(here, "..", ".env.local") });
  } catch (e) { console.error("dotenv load failed:", e.message); }
}

// ───────────────────────── AWS via Vantage ─────────────────────────
async function pullAWS(today) {
  const r = await fetch("https://instances.vantage.sh/instances.json");
  if (!r.ok) throw new Error(`Vantage HTTP ${r.status}`);
  const data = await r.json();
  const rows = [];
  for (const inst of data) {
    const t = inst.instance_type;
    if (!t || !/^(p[2-9]|g[2-9]|trn|inf|dl)/.test(t)) continue;
    const m = classifySku("AWS", t);
    if (!m) continue;
    const region = "us-east-1";
    const od = inst.pricing?.[region]?.linux?.ondemand;
    if (od == null || od === "" || od === "N/A") continue;
    const hourly = parseFloat(od);
    if (!isFinite(hourly) || hourly <= 0) continue;
    rows.push({
      as_of_date: today,
      provider: "AWS",
      instance_sku: t,
      cohort: m.cohort,
      gpus_per_instance: m.gpus,
      region,
      surface: "on_demand",
      hourly_usd: +hourly.toFixed(4),
      hourly_per_gpu_usd: +(hourly / m.gpus).toFixed(4),
    });
  }
  return rows;
}

// ───────────────────────── Azure ─────────────────────────
async function pullAzure(today) {
  const filter = `serviceName eq 'Virtual Machines' and armRegionName eq 'eastus' and contains(armSkuName, 'ND') and priceType eq 'Consumption'`;
  let url = `https://prices.azure.com/api/retail/prices?$filter=${encodeURIComponent(filter)}`;
  const seen = new Map();
  for (let pages = 0; url && pages < 10; pages++) {
    const r = await fetch(url);
    if (!r.ok) throw new Error(`Azure HTTP ${r.status}`);
    const j = await r.json();
    for (const p of j.Items || []) {
      const sku = p.armSkuName;
      if (!sku) continue;
      const m = classifySku("Azure", sku);
      if (!m) continue;
      const surface = /spot/i.test(p.skuName || "") ? "spot"
                    : /low\s*priority/i.test(p.skuName || "") ? "low_priority"
                    : "on_demand";
      const hourly = parseFloat(p.unitPrice);
      if (!isFinite(hourly) || hourly <= 0) continue;
      const key = `${sku}|${p.armRegionName}|${surface}`;
      const prev = seen.get(key);
      if (prev == null || hourly < prev.hourly_usd) {
        seen.set(key, {
          as_of_date: today,
          provider: "Azure",
          instance_sku: sku,
          cohort: m.cohort,
          gpus_per_instance: m.gpus,
          region: p.armRegionName,
          surface,
          hourly_usd: +hourly.toFixed(4),
          hourly_per_gpu_usd: +(hourly / m.gpus).toFixed(4),
        });
      }
    }
    url = j.NextPageLink || null;
  }
  return [...seen.values()];
}

// ───────────────────────── GCP ─────────────────────────
async function pullGCP(today) {
  if (!process.env.GCP_API_KEY) return [];
  const base = `https://cloudbilling.googleapis.com/v1/services/6F81-5844-456A/skus?key=${process.env.GCP_API_KEY}`;
  const rows = [];
  let pageToken = "";
  for (let pages = 0; pages < 20; pages++) {
    const url = base + (pageToken ? `&pageToken=${pageToken}` : "") + "&pageSize=5000";
    const r = await fetch(url);
    if (!r.ok) throw new Error(`GCP HTTP ${r.status}`);
    const j = await r.json();
    for (const sku of j.skus || []) {
      const desc = sku.description || "";
      if (!/\bGPU\b|\bGpu\b/.test(desc)) continue;
      if (/Licensing Fee/i.test(desc)) continue;
      if (sku.category?.usageType !== "OnDemand" && sku.category?.usageType !== "Preemptible") continue;
      const m = classifySku("GCP", desc);
      if (!m) continue;
      const region = (sku.serviceRegions || [])[0] || "global";
      if (!/us-/i.test(region) && region !== "global") continue;
      const tier = sku.pricingInfo?.[0]?.pricingExpression?.tieredRates?.[0]?.unitPrice;
      if (!tier) continue;
      const hourly = Number(tier.units || 0) + Number(tier.nanos || 0) / 1e9;
      if (!isFinite(hourly) || hourly <= 0) continue;
      // GCP collapses many products under usageType="OnDemand". Disambiguate by description.
      let surface;
      if (sku.category?.usageType === "Preemptible" || /Spot Preemptible|\bPreemptible\b/i.test(desc)) {
        surface = "spot";
      } else if (/Calendar Mode/i.test(desc) && !/DWS/i.test(desc)) {
        surface = "reserved";
      } else if (/DWS|Defined Duration/i.test(desc)) {
        surface = "committed_short";
      } else {
        surface = "on_demand";
      }
      rows.push({
        as_of_date: today,
        provider: "GCP",
        instance_sku: desc,
        cohort: m.cohort,
        gpus_per_instance: m.gpus,
        region,
        surface,
        hourly_usd: +hourly.toFixed(4),
        hourly_per_gpu_usd: +(hourly / m.gpus).toFixed(4),
      });
    }
    pageToken = j.nextPageToken || "";
    if (!pageToken) break;
  }
  return rows;
}

// ───────────────────────── handler ─────────────────────────
export default async function handler(req, res) {
  // Vercel cron sends Authorization: Bearer ${CRON_SECRET}.
  // CRON_SECRET is auto-generated by Vercel — see Vercel docs / env vars.
  const auth = req.headers.authorization || "";
  if (process.env.CRON_SECRET && auth !== `Bearer ${process.env.CRON_SECRET}`) {
    return res.status(401).json({ error: "Unauthorized" });
  }

  const supabaseUrl = process.env.SUPABASE_URL || process.env.VITE_SUPABASE_URL;
  const supabaseKey = process.env.SUPABASE_SERVICE_ROLE_KEY;
  if (!supabaseUrl || !supabaseKey) {
    return res.status(500).json({
      error: "Missing Supabase env vars",
      have_url: !!supabaseUrl,
      have_service_role_key: !!supabaseKey,
      env_keys_present: Object.keys(process.env).filter(k => k.startsWith("SUPABASE") || k.startsWith("VITE_SUPABASE") || k.startsWith("GCP") || k.startsWith("CRON")),
    });
  }
  const supabase = createClient(supabaseUrl, supabaseKey, { auth: { persistSession: false } });

  const today = new Date().toISOString().slice(0, 10);
  const summary = { as_of_date: today, providers: {}, total_rows: 0, errors: [] };
  const allRows = [];

  for (const [name, fn] of [["AWS", pullAWS], ["Azure", pullAzure], ["GCP", pullGCP]]) {
    try {
      const rows = await fn(today);
      summary.providers[name] = rows.length;
      allRows.push(...rows);
    } catch (e) {
      summary.errors.push(`${name}: ${e.message}`);
      summary.providers[name] = 0;
    }
  }

  // Neoclouds — static rate cards from src/data/neocloudPrices.js. No live API.
  const neoclouds = NEOCLOUD_PRICES.map(p => ({
    as_of_date: today,
    provider: p.provider,
    instance_sku: p.sku,
    cohort: p.cohort,
    gpus_per_instance: p.gpus,
    region: "us",
    surface: "on_demand",
    hourly_usd: +(p.hourly_per_gpu_usd * p.gpus).toFixed(4),
    hourly_per_gpu_usd: +p.hourly_per_gpu_usd.toFixed(4),
  }));
  for (const r of neoclouds) {
    summary.providers[r.provider] = (summary.providers[r.provider] || 0) + 1;
  }
  allRows.push(...neoclouds);

  // Upsert in batches. PK on (as_of_date, provider, instance_sku, region, surface).
  // Re-running on the same day is idempotent — same key, latest values win.
  const BATCH = 500;
  for (let i = 0; i < allRows.length; i += BATCH) {
    const chunk = allRows.slice(i, i + BATCH);
    const { error } = await supabase.from("compute_prices").upsert(chunk, {
      onConflict: "as_of_date,provider,instance_sku,region,surface",
    });
    if (error) {
      summary.errors.push(`Supabase upsert (batch ${i / BATCH}): ${error.message}`);
      break;
    }
  }
  summary.total_rows = allRows.length;

  return res.status(200).json(summary);
}
